const typeIndexByDimension = {
  E_I: 0,
  S_N: 1,
  T_F: 2,
  J_P: 3,
};

const clamp = (value, min, max) => Math.min(max, Math.max(min, value));

export function allTypes() {
  const a = ['E', 'I'];
  const b = ['S', 'N'];
  const c = ['T', 'F'];
  const d = ['J', 'P'];
  const result = [];
  for (const i of a) for (const j of b) for (const k of c) for (const m of d) result.push(`${i}${j}${k}${m}`);
  return result;
}

export function calculateTypeFromAnswers(answers) {
  const scores = { E: 0, I: 0, S: 0, N: 0, T: 0, F: 0, J: 0, P: 0 };
  for (const q of questions) {
    const val = answers[q.id];
    if (val === undefined) continue;
    if (val > 0) scores[q.agree] += val;
    else if (val < 0) scores[q.disagree] += Math.abs(val);
  }

  return [
    scores.E >= scores.I ? 'E' : 'I',
    scores.S >= scores.N ? 'S' : 'N',
    scores.T >= scores.F ? 'T' : 'F',
    scores.J >= scores.P ? 'J' : 'P',
  ].join('');
}

export function getIdealAnswerForQuestion(question, type) {
  const idx = typeIndexByDimension[question.dimension];
  const target = type[idx];
  return question.agree === target ? 3 : -3;
}

export function buildIdealAnswersForType(type) {
  const answers = {};
  for (const q of questions) answers[q.id] = getIdealAnswerForQuestion(q, type);
  return answers;
}

export function calculateAlignment(actual, ideal) {
  const safeActual = actual ?? 0;
  return clamp(1 - Math.abs(safeActual - ideal) / 6, 0, 1);
}

function scoreLevelText(score) {
  if (score >= 0.85) return '高度契合';
  if (score >= 0.7) return '较高契合';
  if (score >= 0.55) return '中等契合';
  return '可继续观察';
}

function getDimensionScore(answers, type, profile) {
  const scores = profile.questionIds.map((questionId) => {
    const question = questions.find((item) => item.id === questionId);
    const ideal = getIdealAnswerForQuestion(question, type);
    return calculateAlignment(answers[questionId], ideal);
  });
  return scores.reduce((sum, val) => sum + val, 0) / scores.length;
}

export function analyzeAnswers(answers, type) {
  const perQuestionAlignment = questions.map((q) => {
    const ideal = getIdealAnswerForQuestion(q, type);
    return calculateAlignment(answers[q.id], ideal);
  });

  const averageAlignment = perQuestionAlignment.reduce((sum, val) => sum + val, 0) / perQuestionAlignment.length;
  const matchPercent = Math.round(averageAlignment * 100);

  const dimensionBreakdown = dimensionProfiles.map((profile) => {
    const score = getDimensionScore(answers, type, profile);
    return {
      id: profile.id,
      name: profile.name,
      tag: profile.tag,
      description: profile.description,
      score: Number(score.toFixed(3)),
      scorePercent: Math.round(score * 100),
      scoreText: scoreLevelText(score),
      isHit: score >= DIMENSION_HIT_THRESHOLD,
    };
  });

  const hitCount = dimensionBreakdown.filter((item) => item.isHit).length;
  const strengths = [...dimensionBreakdown].sort((a, b) => b.score - a.score).slice(0, 3);
  const watchouts = [...dimensionBreakdown].sort((a, b) => a.score - b.score).slice(0, 2);
  const summaryText = `匹配度 ${matchPercent}% ，精准命中 ${hitCount}/15 维，你的画像已经具备较高可解释性。`;

  return {
    matchPercent,
    hitCount,
    dimensionBreakdown,
    strengths,
    watchouts,
    summaryText,
  };
}
